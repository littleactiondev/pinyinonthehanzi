export function convertToPinyin(text) {
    const result = [];
    
    for (const char of text) {
        // 한자인 경우 병음으로 변환
        if (pinyinMap[char]) {
            result.push(pinyinMap[char]);
        } else if (/[\u4e00-\u9fa5]/.test(char)) {
            // 매핑에 없는 한자는 온라인 API 사용 필요
            result.push(char); // 일단 원문 유지
        } else {
            result.push(null); // 한자가 아님
        }
    }
    
    return result;
}
